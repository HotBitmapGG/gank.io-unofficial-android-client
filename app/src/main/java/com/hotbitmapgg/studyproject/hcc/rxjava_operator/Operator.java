package com.hotbitmapgg.studyproject.hcc.rxjava_operator;

public class Operator
{

    public int id;

    public String name;

    public String desc;

    public String img;

    public String url;

    public int parentId;


    public Operator(int id, String name, String desc, String img, String url, int parentId)
    {
        this.id = id;
        this.name = name;
        this.desc = desc;
        this.img = img;
        this.url = url;
        this.parentId = parentId;
    }
}
